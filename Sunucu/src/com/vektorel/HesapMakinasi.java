package com.vektorel;

public class HesapMakinasi {
	
	public int toplama(int veri1,int veri2){
		return veri1+veri2;
	}
	public int cikarma(int veri1,int veri2){
		return veri1-veri2;
	}
	public int carpma(int veri1,int veri2){
		return veri1*veri2;
	}
	public int bolme(int veri1,int veri2){
		return veri1/veri2;
	}

}
